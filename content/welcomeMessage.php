<p> Welcome to the Image Library, this website has been created on request of Sarah Cooper the course leader of BSc PhotoJournalism. </p>
<p> This website allows students at Leeds Beckett University to upload images to be viewed and purchased by regular users. </p>
<p> This website uses PHP OO classes to achieve functionality such as the login/register system and shopping cart </p>
<p> The website only uses Paypal as a payment solution, after checkout the desired images are emailed to the user via PHP Mail. </p>
